clear, clc,

sv = siteviewer(Basemap="streets-light", Buildings="manhattan.osm");
tx = txsite( ...
    "Latitude",40.71340216569147, ...
    "Longitude",-74.00554655823396, ...
    "AntennaAngle",0, ...
    "AntennaHeight",5, ...
    "TransmitterFrequency",3.5e9);
show(tx)

rx = rxsite( ...
    "Latitude",40.71283494892898, ...
    "Longitude",-74.0069169162511, ...
    "AntennaAngle",0, ...
    "AntennaHeight",1, ...
    "TransmitterFrequency",3.5e9);
show(rx)

%%

pm = propagationModel("raytracing", ...
    "Method","image", ...
    "MaxNumReflections",2);
rays = raytrace(tx,rx,pm);

%%

rtchan = comm.RayTracingChannel(rays{1},tx,rx);
rtchan.SampleRate = 50e6;
showProfile(rtchan);

%%

rays = rays{1,1};
plot(rays)