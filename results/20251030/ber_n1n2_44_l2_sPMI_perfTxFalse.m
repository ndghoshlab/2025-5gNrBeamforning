% Plotting
figure;
numCombinations = length(ModOrderList) * length(angleThresholds);
modColors = lines(length(ModOrderList));
lineStyles = {'-', '--'};
markers = {'s', 'o'};
for combIdx = 1:numCombinations
    % Skip if data is empty (256QAM-sPMI5)
    if isempty(SNRdB_all{combIdx})
        continue;
    end
    
    modIdx = ceil(combIdx / length(angleThresholds));
    angleIdx = mod(combIdx - 1, length(angleThresholds)) + 1;
    semilogy(SNRdB_all{combIdx}, avgBER_all{combIdx}, ... 
        'LineWidth', 2, ...
        'LineStyle', lineStyles{angleIdx}, ...
        'Marker', markers{angleIdx}, ...
        'MarkerSize', 6, ...
        'Color', modColors(modIdx, :), ...
        'DisplayName', combinationLabels{combIdx});
    hold on;
end
grid on;
hold off;
xlabel('SNR (dB)', 'FontSize', 12, 'FontWeight', 'bold');
ylabel('BER', 'FontSize', 12, 'FontWeight', 'bold');
title(sprintf('BER vs SNR (%d slots, perfTx=false)', numIter), ...
    'FontSize', 14, 'FontWeight', 'bold');
